	<div class="article_title">
		<h2>GitHub finds 92% of developers love AI tools</h2>
	</div>
	<div class="article_content">
		<div>
			<img
				class="article_image"
				src="https://regmedia.co.uk/2019/07/20/shutterstock_programmer.jpg?x=302&y=151&crop=1"
				alt="IA"
			/>
		</div>
		<p>
			GitHub, which has been doing a brisk business selling subscriptions to its
			litigation-encumbered Copilot AI helper, surveyed 500 US-based developers to find if any of
			them are already using AI coding tools at work.
		</p>
		<p>As it turns out, they are, and in a big way. We know, you're shocked.</p>
		<p>
			The Microsoft-owned code storage biz recently hired Wakefield Research to interrogate coders
			at companies with more than 1,000 employees about their tools and workflows. The purpose of
			the survey, according to chief product officer, Inbal Shani, is to learn more about
			productivity, collaboration, AI, and operating effectively in enterprise environments.
		</p>
		<p>
			Among the key findings of the report released on Tuesday is that developers have already
			committed to AI assistance, evidently heedless of
			<a href="https://www.theregister.com/2023/06/09/github_copilot_lawsuit/" target="_blank">
				legal challenges
			</a>
			that could limit such tools on copyright grounds.
		</p>
		<p>
			<i>
				"A staggering 92 percent of US-based developers working in large companies report using an
				AI coding tool either at work or in their personal time – and 70 percent say they see
				significant benefits to using these tools",
			</i>
			the study says.
		</p>
		<p>
			Interestingly, the report indicates that developers believe AI tools will improve the code
			they create.
		</p>
		<p>
			<i>
				"Developers say AI coding tools can help them meet existing performance standards with
				improved code quality, faster outputs, and fewer production-level incidents"
			</i>
			the survey says.
		</p>
		<p>
			Respondents may not be aware of academic studies to the contrary. One, for example, found AI
			helpers like ChatGPT produce code that is
			<i>
				"<a href="https://www.theregister.com/2023/04/21/chatgpt_insecure_code/" target="_blank">
					well below
				</a>
				minimal security standards applicable in most contexts."
			</i>
			Another found that Copilot
			<a href="https://www.theregister.com/2022/12/21/ai_assistants_bad_code/" target="_blank">
				produced more security vulnerabilities
			</a>
			than code created without AI help, while developers mistakenly thought the machine-learning tools
			produced fewer mistakes. A third study found Copilot produced security bugs
			<a href="https://www.theregister.com/2021/08/25/github_copilot_study/" target="_blank">
				about 40 percent
			</a>
			of the time.
		</p>
		<p>
			Fittingly, those responding to GitHub's survey questions appear to believe that less weight
			should be given to code quality as a performance metric. Asked how they are currently judged,
			these programmers responded: code quality (40 percent); time to complete a task (34 percent);
			number of production incidents (34 percent); lines of code written (33 percent); and number of
			bugs or issues resolved (33 percent).
		</p>
		<p>
			Assuming AI coding tools are used, these devs would prefer to be judged on the basis of: code
			quality (36 percent); time to complete a task (36 percent); number of production incidents (33
			percent); lines of code written (33 percent); and number of pull requests (32 percent).
		</p>
		<p>
			Speedier and shoddier seems to be the order of the day. And in a further example of
			self-interest, the same portion of devs who say they're judged on lines of code written
			believe that metric should continue, even under the presumed productivity enhancement of an AI
			helper.
		</p>
		<p>
			GitHub's
			<a
				href="https://github.blog/2023-06-13-survey-reveals-ais-impact-on-the-developer-experience/"
				target="_blank"
			>
				survey
			</a>
			suggests this may be worth reconsidering:
			<i>
				"With the increase of AI tooling being used in software development – which often
				contributes to code volume – engineering leaders will need to ask whether measuring code
				volume is still the best way to measure productivity and output."
			</i>
		</p>
		<p>
			Many of the surveyed devs (81 percent) also see AI as a path toward greater collaboration
			within teams and organizations. Asked where teams armed with AI tools should focus, they said:
			code reviews (45 percent); security reviews (45 percent); planning (43 percent); pair
			programming (43 percent); solution design (42 percent).
		</p>
		<p>
			When questioned on how AI tools can help their daily work, the devs said: develop your
			language coding skills (57 percent); become more productive (53 percent); focus on
			building/creating vs. repetitive tasks (51 percent); and prevent burnout (41 percent).
		</p>
		<p>
			The survey concludes,
			<i>
				"Developer satisfaction, productivity, and organizational impact are all positioned to get a
				boost from AI coding tools – and that will have a material impact on the overall developer
				experience."
			</i>
			Just wait until you hear how much writers adore autocorrect and spell check.
		</p>
